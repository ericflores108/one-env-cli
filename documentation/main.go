package main

import (
	"log"

	"github.com/ericflores108/one-env-cli/cmd"
	"github.com/spf13/cobra/doc"
)

func main() {
	header := &doc.GenManHeader{
		Title:  "Audiofile",
		Source: "Auto generated by ericflores108",
	}
	err := doc.GenManTree(cmd.RootCMD(), header, "./pages")
	if err != nil {
		log.Fatal(err)
	}
}
